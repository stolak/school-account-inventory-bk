  /**
   * Calculate invoice total from items
   */
  async calculateInvoiceTotal(invoiceId: string) {
    try {
      const items = await prisma.item.findMany({
        where: { invoiceId },
      });

      const total = items.reduce((sum, item) => {
        return sum + Number(item.amount) * item.quantity;
      }, 0);

      return {
        success: true,
        data: { total, itemCount: items.length },
      };
    } catch (error: any) {
      return {
        success: false,
        message: error.message || "Failed to calculate invoice total",
      };
    }
  }

  /**
   * Get invoice statistics for a merchant
   */
  async getMerchantInvoiceStats(merchantId: string) {
    try {
      const [total, pending, paid, cancelled] = await Promise.all([
        prisma.invoice.count({ where: { merchantId } }),
        prisma.invoice.count({
          where: { merchantId, status: InvoiceStatus.Pending },
        }),
        prisma.invoice.count({
          where: { merchantId, status: InvoiceStatus.Paid },
        }),
        prisma.invoice.count({
          where: { merchantId, status: InvoiceStatus.Cancelled },
        }),
      ]);

      const totalAmount = await prisma.invoice.aggregate({
        where: { merchantId },
        _sum: { amount: true },
      });

      const paidAmount = await prisma.invoice.aggregate({
        where: { merchantId, status: InvoiceStatus.Paid },
        _sum: { amount: true },
      });

      const pendingAmount = await prisma.invoice.aggregate({
        where: { merchantId, status: InvoiceStatus.Pending },
        _sum: { amount: true },
      });

      return {
        success: true,
        data: {
          counts: {
            total,
            pending,
            paid,
            cancelled,
          },
          amounts: {
            total: Number(totalAmount._sum.amount || 0),
            paid: Number(paidAmount._sum.amount || 0),
            pending: Number(pendingAmount._sum.amount || 0),
          },
        },
      };
    } catch (error: any) {
      return {
        success: false,
        message: error.message || "Failed to fetch invoice statistics",
      };
    }
  }
