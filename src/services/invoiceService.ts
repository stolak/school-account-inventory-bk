/**
 * Generate a short alphanumeric reference ID (max 24 characters)
 * @returns A string containing only alphanumeric characters (0-9, A-Z)
 */
export const generateShortReferenceId = (): string => {
  // Generate 12 random bytes (96 bits) for good entropy
  const bytes = randomBytes(12);
  // Convert to hex and then map to alphanumeric (0-9, A-Z)
  // Hex gives us 24 characters, we'll convert a-f to A-F and keep 0-9
  const hex = bytes.toString("hex").toUpperCase();

  // Take first 24 characters (hex is always 24 chars for 12 bytes)
  // Hex already contains only 0-9 and A-F, which are alphanumeric
  return hex.substring(0, 24);
};

