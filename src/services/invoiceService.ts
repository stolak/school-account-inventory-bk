  async validateMandate({
    invoiceId,
    referenceId,
  }: {
    invoiceId?: string;
    referenceId?: string;
  }) {
    let mandate: any;

    if (referenceId) {
      mandate = await invoiceMandateService.getMandateByReferenceId(
        referenceId
      );
    }
    if (invoiceId) {
      const mandates = await invoiceMandateService.getMandatesByInvoiceId(
        invoiceId
      );
      if (mandates?.length > 0) {
        mandate = mandates[0];
      }
    }
    if (!mandate) {
      throw new Error("Mandate not found");
    }
    const mandateId = mandate.monoMandateId;
    const mandateStatus = mandate.status;
    const mandateAmount = mandate.amount;
    const mandateTenure = mandate.tenure;
    const mandateDownPayment = mandate.downPayment;
    const mandateReferenceId = mandate.referenceId;
    const mandateInvoiceId = mandate.invoiceId;
    const mandateBuyerId = mandate.buyerId;
    const mandateMonoAccountId = mandate.monoAccountId;
    const mandateMonoCustomerId = mandate.monoCustomerId;
    const invoiceAmount = mandate.invoice.amount;
    const monoMandate = await accountDetailsService.getMonoMandateById(
      mandateId
    );
    if (!monoMandate) {
      throw new Error("Mono mandate not found");
    }
    const monoMandateStatus = monoMandate.data?.data?.status;
    const monoMandateApproved = monoMandate.data?.data?.approved;
    if (monoMandateApproved) {
      //upd
      await invoiceMandateService.updateInvoiceMandate(mandate.id, {
        status: MandateStatus.Active,
      });
    }
    const eligibilityAndScore =
      await loanSettingService.liveEligibilityPurchase({
        purchaseAmount: Number(invoiceAmount),
        downPaymentAmount: mandateDownPayment,
        months: mandateTenure,
        buyerId: mandateBuyerId,
      });
    if (!eligibilityAndScore) {
      throw new Error("Eligibility and score not found");
    }

    return {
      ...monoMandate,
      eligibilityAndScore: {
        ...eligibilityAndScore,
        invoiceId: mandateInvoiceId,
        mandateId,
        referenceId,
      },
    };
  }

  async initiateUpfrontPayment(invoiceId: string) {
    const referenceId = generateShortReferenceId();
    const invoice = await prisma.invoice.findUnique({
      where: { id: invoiceId },
    });
    if (!invoice) {
      throw new Error("Invoice not found");
    }
    const mandate = await invoiceMandateService.getMandatesByInvoiceId(
      invoiceId
    );
    if (mandate?.length === 0) {
      throw new Error("Mandate not found");
    }
    const mandateDownPayment = mandate[0].downPayment;
    const mandateTenure = mandate[0].tenure;
    const mandateBuyerId = mandate[0].buyerId;
    const mandateMonoAccountId = mandate[0].monoAccountId;
    const mandateMonoCustomerId = mandate[0].monoCustomerId;
    const eligibilityAndScore =
      await loanSettingService.liveEligibilityPurchase({
        purchaseAmount: Number(invoice.amount),
        downPaymentAmount: Number(mandateDownPayment),
        months: Number(mandateTenure),
        buyerId: mandateBuyerId,
      });
    if (!eligibilityAndScore) {
      throw new Error("Eligibility and score not found");
    }

    // check if direct pay exists for the mandate
    await directPayService.regenerateDirectPay(
      mandate[0].id
    );
   
    // Check if the result has the required properties (complex type)
    // Type guard to narrow the type
    if (
      typeof eligibilityAndScore !== "object" ||
      !("requiredDownPayment" in eligibilityAndScore) ||
      !("adminCharge" in eligibilityAndScore) ||
      !("insurance" in eligibilityAndScore)
    ) {
      throw new Error(
        "Eligibility and score result does not contain required payment information"
      );
    }

    // TypeScript now knows these properties exist
    const result = eligibilityAndScore as {
      requiredDownPayment: number;
      adminCharge: number;
      insurance: number;
    };

    const amount =
      Number(result.requiredDownPayment) +
      Number(result.adminCharge) +
      Number(result.insurance);

    const directPay = await directPayService.initiateMonoDirectPay({
      amount: amount * 100,
      customerId: mandateMonoCustomerId,
      reference: referenceId,
      account: mandateMonoAccountId,
      description: `Upfront payment for invoice ${invoice.liftpayId}`,
    });
    if (!directPay.success) {
      throw new Error("Failed to initiate upfront payment");
    }
    // create direct pay
    const createdDirectPay = await directPayService.createDirectPay({
      invoiceId: invoiceId,
      mandateId: mandate[0].id,
      amount: amount,
      buyerId: mandateBuyerId,
      monoCustomerId: mandateMonoCustomerId,
      reference: referenceId,
      monoAccountId: mandateMonoAccountId,
      monoUrl: directPay.data.data.mono_url,
      status: DirectPayStatus.Pending,
    });
    if (!directPay.success) {
      throw new Error("Failed to create direct pay");
    }
    return {
      success: true,
      message: "Upfront payment initiated successfully",
      data: {
        directPay: { ...createdDirectPay, invoiceId, referenceId },
      },
    };
  }
  async validateMandateMandateDownPaymentAndCreateLoanInvoice({
    invoiceId,
    referenceId,
  }: {
    invoiceId?: string;
    referenceId?: string;
  }) {
    let mandate;
    if (invoiceId) {
      const invoiceMandate = await invoiceMandateService.getMandatesByInvoiceId(
        invoiceId
      );
      if (invoiceMandate?.length > 0) {
        mandate = invoiceMandate[0];
      }
    }
    if (referenceId) {
      try {
        mandate = await invoiceMandateService.getMandateByReferenceId(
          referenceId
        );
      } catch (error) {
        const directPay = await directPayService.getDirectPayByReference(
          referenceId
        );
        if (!directPay) {
          throw new Error("Direct pay not found");
        }
        const invoiceMandate =
          await invoiceMandateService.getMandatesByInvoiceId(
            directPay.invoiceId
          );
        if (invoiceMandate?.length > 0) {
          mandate = invoiceMandate[0];
        }
      }
    }

    if (!mandate) {
      throw new Error("Mandate not found");
    }
    if (mandate.status != "Active") {
      throw new Error("Mandate emandate not yet authorise");
    }
    // check upfront payment
    const upfrontPayment = await directPayService.getDirectPaysByMandateId(
      mandate.id
    );
    if (!upfrontPayment) {
      throw new Error("Upfront payment not found");
    }
    const finalizeResult = await this.finalizeAfterUpfrontAndMandate(
      mandate.id
    );
    return finalizeResult;
  }
