  /**
   * Update invoice
   */
  async finalizeAfterUpfrontAndMandate(mandateId: string) {
    // Re-fetch mandate and verify upfront payment if needed, then update invoice, create loan, and record revenue/merchant transaction
    const mandate = await prisma.invoiceMandate.findUnique({
      where: { id: mandateId },
    });
    if (!mandate) {
      throw new Error("Mandate not found");
    }

    // check upfront payment
    const upfrontPayment = await directPayService.getDirectPaysByMandateId(
      mandate.id
    );
    if (!upfrontPayment) {
      throw new Error("Upfront payment not found");
    }
    if (upfrontPayment?.length > 0 && upfrontPayment[0].status != "Completed") {
      const validateUpfrontPayment = await directPayService.verifyMonoDirectPay(
        upfrontPayment[0].reference
      );
      if (
        !validateUpfrontPayment.success &&
        validateUpfrontPayment.data.status !== "successful"
      ) {
        return {
          success: false,
          message: "Upfront payment not yet completed",
          data: validateUpfrontPayment.data,
        };
      }
    }
    const buyerId = mandate.buyerId;
    const mandateInvoiceId = mandate.invoiceId;
    const mandateDownPayment = mandate.downPayment;
    const mandateTenure = mandate.tenure;

    const buyer = await prisma.buyer.findUnique({
      where: { id: buyerId },
    });
    if (!buyer) {
      throw new Error("Buyer not found");
    }
    const invoice = await prisma.invoice.findUnique({
      where: { id: mandateInvoiceId },
    });
    if (!invoice) {
      throw new Error("Invoice not found");
    }
    const eligibilityAndScore =
      await loanSettingService.liveEligibilityPurchase({
        purchaseAmount: Number(invoice.amount),
        downPaymentAmount: Number(mandateDownPayment),
        months: Number(mandateTenure),
        buyerId: buyerId,
      });
    if (!eligibilityAndScore) {
      throw new Error("Eligibility and score not found");
    }

    const updateData: any = {
      status: InvoiceStatus.Paid,
      buyerId: buyerId,
      customerName: `${buyer.firstName} ${buyer.lastName}`,
      customerEmail: buyer.email,
      customerPhoneNumber: buyer.phoneNumber,
    };
    

    if (!("approved" in eligibilityAndScore) || !eligibilityAndScore.approved) {
      return {
        success: false,
        message: "Eligibility and score not valid",
        data: eligibilityAndScore,
      };
    }

    const updatedInvoice = await prisma.invoice.update({
      where: { id: mandateInvoiceId },
      data: updateData,
      include: {
        items: true,
      },
    });

    // get loan by invoice number
    const loan = await loanService.getLoanByInvoiceId(mandateInvoiceId);
    if (loan && loan.success) {
      return {
        success: true,
        message: "Invoice updated successfully",
        data: {
          invoice: updatedInvoice,
          loan: loan.data,
        },
      };
    }
    const loanResult = await loanService.createLoan({
      buyerId: buyerId,
      loanAmount: eligibilityAndScore.approvedLoanAmount,
      loanTenure: Number(mandateTenure),
      loanStartDate: new Date(),
      loanStatus: LoanStatus.Active,
      loanType: LoanType.Personal,
      loanInterestRate: Number(INTERST_RATE),
      loanPurpose: "Loan",
      purchaseAmount: Number(invoice.amount),
      downPaymentAmount: eligibilityAndScore.requiredDownPayment,
      merchantId: invoice.merchantId,
      referenceNumber: invoice.liftpayId,
      adminCharge: eligibilityAndScore.adminCharge,
      insurance: eligibilityAndScore.insurance,
      monthlyRepayment: eligibilityAndScore.monthlyRepayment,
      invoiceId: mandateInvoiceId,
    });

    if (!loanResult.success) {
      return {
        success: false,
        message:
          loanResult.error || "Failed to create loan after approving invoice",
      };
    }
    // perform merchant payment
    await merchantTransactionService.createMerchantTransaction({
      merchantId: invoice.merchantId,
      invoiceRef: invoice.id,
      credit: Number(invoice.amount),
      debit: 0,
      transactionType: MerchantTransactionType.Credit,
      description: `invoice credit on ${invoice.id}`,
      status: TransactionStatus.Completed,
      transactionDate: new Date(),
      groupReference: invoice.liftpayId,
    });
    await revenueService.createRevenue({
      type: RevenueType.DownPayment,
      credit: eligibilityAndScore.requiredDownPayment,
      debit: 0,
      description: `Down payment for loan ${invoice.liftpayId}`,
      parentTable: "invoice",
      referenceIds: [invoice.id],
      detail: `Down payment for loan ${invoice.liftpayId}`,
      buyerId: buyerId,
      transactionDate: new Date(),
    });

    return {
      success: true,
      message: "Invoice updated successfully",
      data: {
        invoice: updatedInvoice,
        loan: loanResult.data,
      },
    };
  }
  async updateInvoice(id: string, input: UpdateInvoiceInput) {
    try {
      const existingInvoice = await prisma.invoice.findUnique({
        where: { id },
      });

      if (!existingInvoice) {
        throw new Error("Invoice not found");
      }

      // Validate buyer if provided
      if (input.buyerId) {
        const buyer = await prisma.buyer.findUnique({
          where: { id: input.buyerId },
        });

        if (!buyer) {
          throw new Error("Buyer not found");
        }
      }

      const updateData: any = {};
      if (input.customerName !== undefined)
        updateData.customerName = input.customerName;
      if (input.customerEmail !== undefined)
        updateData.customerEmail = input.customerEmail;
      if (input.customerPhoneNumber !== undefined)
        updateData.customerPhoneNumber = input.customerPhoneNumber;
      if (input.dueDate !== undefined)
        updateData.dueDate = new Date(input.dueDate);
      if (input.note !== undefined) updateData.note = input.note;
      if (input.amount !== undefined) updateData.amount = input.amount;
      if (input.buyerId !== undefined) updateData.buyerId = input.buyerId;
      if (input.status !== undefined) updateData.status = input.status;
      if (input.type !== undefined) updateData.type = input.type;

      const invoice = await prisma.invoice.update({
        where: { id },
        data: updateData,
        include: {
          items: true,
          buyer: {
            select: {
              id: true,
              liftpayId: true,
              firstName: true,
              lastName: true,
              email: true,
            },
          },
          merchant: {
            select: {
              id: true,
              liftpayId: true,
              businessName: true,
              businessEmail: true,
            },
          },
        },
      });
      //update items
      await prisma.item.deleteMany({
        where: { invoiceId: id },
      });
      if (input.items) {
        for (const item of input.items) {
          await prisma.item.create({
            data: {
              invoiceId: id,
              description: item.description || "",
              quantity: item.quantity || 0,
              amount: item.amount || 0,
            },
          });
        }
      }

      return {
        success: true,
        message: "Invoice updated successfully",
        data: invoice,
      };
    } catch (error: any) {
      return {
        success: false,
        message: error.message || "Failed to update invoice",
      };
    }
  }

